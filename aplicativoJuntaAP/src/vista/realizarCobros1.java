        int cont = 0;
        if (jTextField_codigoUsuario.getText().isEmpty()) {
            cont++;
            JOptionPane.showMessageDialog(this, "Debe llenar ingresar el Socio");
        }
        if (fechaCobro.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fecha");
            cont++;
        }
        if (cont == 0) {
            try {

                // boton para llenar en la base
                ArrayList Resultados = new ArrayList();
                metodos m = new metodos();
                String nombre = jTextField_codigoUsuario.getText();
                if (isNumeric(nombre)) {
                    Resultados = m.obtenerInformacionSocioCod(nombre);
                } else {
                    Resultados = m.obtenerInformacionSocioNombre(nombre);
                }
                if (!Resultados.isEmpty()) {
                    llenarTabla(Resultados);
                } else {
                    JOptionPane.showMessageDialog(this, "USUARIO NO REGISTRADO");
                }
            } catch (SQLException | IOException ex) {
                Logger.getLogger(realizarCobros1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
