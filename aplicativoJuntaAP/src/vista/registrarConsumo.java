    private void jButton_guardarConsumoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarConsumoActionPerformed
        // TODO add your handling code here:ArrayList<String> valores = new ArrayList<>();

        int año = jDateChooser_selectorFechaTrabajo.getCalendar().get(Calendar.YEAR);
        int mes = jDateChooser_selectorFechaTrabajo.getCalendar().get(Calendar.MARCH);
        int mesActual = jDateChooser_selectorFechaTrabajo.getCalendar().get(Calendar.MARCH) + 1;
        Calendar calendario = Calendar.getInstance();
        calendario.set(año, mes, 1);
        int ultimoDiaMes = calendario.getActualMaximum(Calendar.DAY_OF_MONTH);
        String fecha = (año + "-" + mesActual + "-" + ultimoDiaMes);
        int fila = TablaSocioConsumo.getRowCount();
        int i;
        ArrayList<Object> valores = new ArrayList<>();
        valores.add(fecha);
        int cont = 0;
        for (i = 0; i < fila; i++) {
            if ((String) TablaSocioConsumo.getValueAt(i, 4) != null) {
                String codigo = (String) TablaSocioConsumo.getValueAt(i, 0);
                String consumoAn = (String) TablaSocioConsumo.getValueAt(i, 3);
                String consumoA = (String) TablaSocioConsumo.getValueAt(i, 4);
                if (isNumeric(TablaSocioConsumo.getValueAt(i, 4).toString())) {
                    double diferencia = Double.parseDouble(consumoA) - Double.parseDouble(consumoAn);
                    valores.add(codigo);
                    valores.add(diferencia);
                    valores.add(consumoA);

                } else {
                    JOptionPane.showMessageDialog(null, "ERROR EN LOS VALORES, PUEDEN QUE CONTENGAN LETRAS", "Alerta!", JOptionPane.ERROR_MESSAGE);
                    cont++;
                }
            } else {
                JOptionPane.showMessageDialog(null, "HAY CAMPOS VACIOS, AGREGAR TODOS LOS VALORES", "Alerta!", JOptionPane.ERROR_MESSAGE);
                cont++;
            }
        }
        if (cont == 0) {
            try {
                metodos.registrarConsumo(valores);
                JOptionPane.showMessageDialog(this, "CONSUMOS REGISTRADOS CORRECTAMENTE");
                System.out.println("EXITO");
                metodos.registrarPagos(valores);
            } catch (SQLException ex) {
                //Logger.getLogger(registrarConsumo.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Consumos ya registrados si desea modificar ingrese en MODIFICAR CONSUMOS", "Alerta!", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton_guardarConsumoActionPerformed

