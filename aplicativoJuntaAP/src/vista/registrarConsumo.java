    public void cargarDatos() {
        ArrayList<String> valores = new ArrayList<>();
        int anio = jDateChooser_selectorFechaTrabajo.getCalendar().get(Calendar.YEAR);
        int mes = jDateChooser_selectorFechaTrabajo.getCalendar().get(Calendar.MONTH) + 1;
        System.out.println("MEEES" + mes);
        int dia = jDateChooser_selectorFechaTrabajo.getCalendar().get(Calendar.DAY_OF_MONTH);
        System.out.println();
        try {
            valores = metodos.informacionSocioConsumoAnterior(mes, anio);
            DefaultTableModel modelo;
            modelo = new DefaultTableModel() {
                public boolean isCellEditable(int filas, int columnas) {
                    if (columnas == 4) {
                        return true;
                    } else {
                        return false;
                    }
                }
            };

            modelo.addColumn("Codigo");
            modelo.addColumn("Num. Medidor");
            modelo.addColumn("Usuario");
            modelo.addColumn("Consumo Anterior");
            modelo.addColumn("Consumo Actual");
            TablaSocioConsumo.setModel(modelo);

            for (int i = 0; i < valores.size() - 1; i = i + 4) {
                modelo.addRow(new Object[]{valores.get(i), valores.get(i + 1), valores.get(i + 2), valores.get(i + 3)});
                // System.out.println(valores.get(i));
            }
            int[] anchos = {40, 40, 200, 40, 40};

            for (int i = 0; i < TablaSocioConsumo.getColumnCount(); i++) {
                TablaSocioConsumo.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);

            }
        } catch (SQLException ex) {
            Logger.getLogger(informacionReuniones.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public static boolean isNumeric(String cadena) {

        boolean resultado;

        try {
            Double.parseDouble(cadena);
            resultado = true;
        } catch (NumberFormatException excepcion) {
            resultado = false;
        }

        return resultado;
    }

