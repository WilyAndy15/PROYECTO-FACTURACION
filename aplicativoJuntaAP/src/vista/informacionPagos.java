    private void jTextField_codigoUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField_codigoUsuarioActionPerformed
        if (jTextField_codigoUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el ID del Socio");
        } else {
            if (isNumeric(jTextField_codigoUsuario.getText())) {
                ArrayList socio = new ArrayList();
                try {
                    socio = metodos.verSocioC(Integer.parseInt(jTextField_codigoUsuario.getText()));
                } catch (SQLException ex) {
                    //Logger.getLogger(informacionPagos.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "No Existen usuarios con ese ID");
                } catch (IOException ex) {
                    //Logger.getLogger(informacionPagos.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "No Existen usuarios con ese ID");
                }
                if (!socio.isEmpty()) {
                    jTextField_nombres.setText(socio.get(2).toString());
                    try {
                        obtenerPagos(Integer.parseInt(socio.get(0).toString()));
                    } catch (SQLException ex) {
                        Logger.getLogger(informacionPagos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No Existen usuarios con ese ID");

                }

            } else {
                JOptionPane.showMessageDialog(this, "ID inválido");
            }

        }
    }//GEN-LAST:event_jTextField_codigoUsuarioActionPerformed

