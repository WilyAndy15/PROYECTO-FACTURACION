    public void vaciarTabla() {
        DefaultTableModel modelo;
        modelo = new DefaultTableModel();
        modelo.addColumn("Fecha");
        modelo.addColumn("Estado");
        jTable_verPagos.setModel(modelo);
    }

    public void obtenerPagos(int idSocio) throws SQLException {
        vaciarTabla();
        ArrayList<String> pagos = metodos.obtenerPagos(idSocio);
        if (!pagos.isEmpty()) {
            DefaultTableModel modelo;
            modelo = new DefaultTableModel() {
                public boolean isCellEditable(int filas, int columnas) {
                    return false;
                }
            };
            modelo.addColumn("Fecha");
            modelo.addColumn("Estado");
            jTable_verPagos.setModel(modelo);
            for (int i = 0; i < pagos.size() - 1; i = i + 2) {
                if (pagos.get(i + 1).equals("0")) {
                    modelo.addRow(new Object[]{pagos.get(i), "No pagado"});
                } else if (pagos.get(i + 1).equals("1")) {
                    modelo.addRow(new Object[]{pagos.get(i), "Pagado"});
                }
            }
        }
    }

