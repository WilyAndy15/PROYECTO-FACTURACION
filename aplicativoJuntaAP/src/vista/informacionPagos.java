    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cont = 0;
        if (jTextField_codigoUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un usuario");
            cont++;
        }
        if (cont == 0) {
            int id = Integer.parseInt(jTextField_codigoUsuario.getText());
            JFileChooser chooser = new JFileChooser();
            int seleccion = chooser.showOpenDialog(this);
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                File fichero = chooser.getSelectedFile();
                metodos.crearReporteUsuario(id, fichero);
                JOptionPane.showMessageDialog(this, "Reporte Generado");
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

