    public void obtenerUsuario() {
        int cont = 0;
        if (fechaCobro.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fecha");
            cont++;
        }
        if (cont == 0) {
            String codigoUsuario = jTextField_codigoUsuario.getText();
            ArrayList<String> usuario = new ArrayList<>();
            if (isNumeric(codigoUsuario)) {
                try {
                    usuario = metodos.obtenerUnicoUsuario(codigoUsuario);
                } catch (SQLException ex) {
                    Logger.getLogger(Reinstalacion1.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    usuario = metodos.obtenerUsuarios(codigoUsuario);
                } catch (SQLException ex) {
                    Logger.getLogger(Reinstalacion1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (!usuario.isEmpty()) {
                DefaultTableModel modelo;
                modelo = new DefaultTableModel();
                modelo.addColumn("Nombre");
                jTable1.setModel(modelo);
                for (int i = 0; i < usuario.size(); i++) {
                    modelo.addRow(new Object[]{usuario.get(i)});
                }

            } else {
                JOptionPane.showMessageDialog(this, "Usuario no registrado");
            }
        }
    }

    public static boolean isNumeric(String cadena) {

        boolean resultado;

        try {
            Integer.parseInt(cadena);
            resultado = true;
        } catch (NumberFormatException excepcion) {
            resultado = false;
        }

        return resultado;
    }

