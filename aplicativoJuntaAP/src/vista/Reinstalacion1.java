    private void jButton_guardarAsistenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarAsistenciaActionPerformed
        int cont = 0;
        int ano = 0, mes = 0, dia = 0;

        ano = fechaCobro.getCalendar().get(Calendar.YEAR);
        mes = fechaCobro.getCalendar().get(Calendar.MONTH) + 1;
        dia = fechaCobro.getCalendar().get(Calendar.DAY_OF_MONTH);

        String fecha = ano + "-" + mes + "-" + dia;
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        int fila = jTable1.getSelectedRow();
        int id = 0, asistencia = 0;
        double cantidad = 0;
        if (ValorReingreso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor");
        } else {
            if (isDouble(ValorReingreso.getText())) {
                cantidad = Double.parseDouble(ValorReingreso.getText());
                String nombres, registro;
                // String fecha = (String) jComboBox_reuniones.getSelectedItem();
                manejador mn = new manejador();
                if (fila >= 0) {
                    nombres = String.valueOf(tm.getValueAt(jTable1.getSelectedRow(), 0));
                    try {
                        id = Integer.parseInt(metodos.obtenerID(nombres));
                        metodos.insertarReinstalacion(id, cantidad, fecha);
                        JOptionPane.showMessageDialog(null, "Valor Ingresado Correctamente");
                    } catch (SQLException ex) {
                        Logger.getLogger(Reinstalacion1.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(null, "Valor Incorrecto");
                    }

                }
            } else {
                JOptionPane.showMessageDialog(null, "Valor de Reingreso Incorrecto");
            }
        }

    }//GEN-LAST:event_jButton_guardarAsistenciaActionPerformed

